public class Endereco
{
    private string rua;
    private string cidade;
    private string estado;

    public string Rua
    {
        get { return rua; }
        set { rua = value; }
    }

    public string Cidade
    {
        get { return cidade; }
        set { cidade = value; }
    }

    public string Estado
    {
        get { return estado; }
        set { estado = value; }
    }
}